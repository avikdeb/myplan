package com.avik.myplan.beans;

public class Role {
	
	private int idrole;
	private String role;
	
	public Role() {
		
		idrole = 0;
		role = "";
	}

	public int getIdrole() {
		return idrole;
	}

	public void setIdrole(int idrole) {
		this.idrole = idrole;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

}
